# peso en gramos
peso_payaso = 112 # peso de un payaso en gramos
peso_muñeca = 75  # peso de una muñeca en gramos

# cantidades de productos pedidos por el cliente
cantidad_payasos = 23
cantidad_muñecas = 54 

# calcular el peso total
peso_total_payasos = peso_payaso * cantidad_payasos
peso_total_muñecas = peso_muñeca * cantidad_muñecas
peso_total = peso_total_payasos + peso_total_muñecas

# mostrar el resultado
print(f"El peso total del pedido es: {peso_total} gramos")